import java.util.Scanner;

public class OmkarAndCompletion {
    private static Scanner s = new Scanner(System.in);

    public static void main(String[] args) {
        int T= s.nextInt();
        for(int z=0;z<T;z++){
            int n = s.nextInt();
            for(int i=0;i<n;i++){
                System.out.print(1+" ");
            }
            System.out.println();
        }
    }

}
